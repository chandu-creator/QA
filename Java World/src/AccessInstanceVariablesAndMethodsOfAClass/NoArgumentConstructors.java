package AccessInstanceVariablesAndMethodsOfAClass;

public class NoArgumentConstructors {
	/*
	 * As the name specifies the no argument constructors of Java does not accept
	 * any parameters instead, using these constructors the instance variables of a
	 * method will be initialized with fixed values for all object
	 */
	int num;

	NoArgumentConstructors() {
		num = 100;
	}
}